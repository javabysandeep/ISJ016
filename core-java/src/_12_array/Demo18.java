package _12_array;

public class Demo18 {
    public static void main(String[] args) {
        int[][] arrays = {
                {1, 2, 3},
                {4, 5, 6, 11},
                {7, 8, 9}
        };
        System.out.println(arrays[0][0] + " " + arrays[0][1] + " " + arrays[0][2]);
        System.out.println(arrays[1][0] + " " + arrays[1][1] + " " + arrays[1][2] + " " + arrays[1][3]);
        System.out.println(arrays[2][0] + " " + arrays[2][1] + " " + arrays[2][2]);
    }

}
