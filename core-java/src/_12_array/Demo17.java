package _12_array;

public class Demo17 {
    public static void main(String[] args) {
        int[][] arrays = {
                {1, 2, 3},
                {4, 5, 6, 11},
                {7, 8, 9}
        };
        System.out.println(arrays);//hash
        System.out.println(arrays.length);//3
        System.out.println(arrays[0]);//hash
        System.out.println(arrays[1]);//hash
        System.out.println(arrays[2]);//hash

        System.out.println("length");
        System.out.println(arrays[0].length);
        System.out.println(arrays[1].length);
        System.out.println(arrays[2].length);
    }

}
