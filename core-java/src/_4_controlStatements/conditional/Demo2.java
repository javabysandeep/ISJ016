package _4_controlStatements.conditional;

public class Demo2 {
    public static void main(String[] args) {
        int number = 100;

        if (number % 2 == 0) {
            System.out.println("The number is even " + number);
        }

        if (number % 2 != 0) {
            System.out.println("The number is odd " + number);
        }
    }
}
